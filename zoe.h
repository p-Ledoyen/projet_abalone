void copy_tab(CASE T[11][21], CASE T_variante[11][21])
{
	int l,c;
	for (l=0;l<11;l++)
	{
		for (c=0;c<21;c++)
		{
			T[l][c]=T_variante[l][c];
		}
	}
}
	
void init_tab(POSITION_DEP pos)
{
	switch (pos)
	{
		case marguerite: copy_tab(T,T_marguerite);
						 break;
		case domination: copy_tab(T,T_domination);
						 break;
		default: break;			 
	}
}
	
